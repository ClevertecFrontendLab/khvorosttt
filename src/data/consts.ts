export function rusName(filterType: string, item: string) {
    switch (filterType) {
        case 'category':
            return markerFood[item];
        case 'author':
            return item;
        case 'meatType':
            return russianMeatType[item];
        case 'sideDishType':
            return russianSideDishType[item];
        case 'allergens':
            return russianAllergens[item];
    }
}
